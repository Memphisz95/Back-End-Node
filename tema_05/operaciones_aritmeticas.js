// Principales operaciones aritméticas
let a = 4;
let b = 2;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a ** b);

// Representación de los números de cadenas de texto
console.log(typeof a);
let cadena = a.toString();
console.log(typeof cadena);

// Redondeo de números decimales
let c = 5.3;
let r = c * 3;
console.log(r);
console.log(r.toFixed(2));
console.log(r.toPrecision(4));