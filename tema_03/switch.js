// switch

let dia = "jueves";

switch(dia) {
    case "lunes":
        console.log("lunes");
        break;
    case "martes": 
        console.log("martes");
        break;
    case "miercoles":
        console.log("miercoles"); 
        break;
    case "jueves":
        console.log("jueves");
        break;
    case "viernes":
        console.log("viernes");
        break;
    default:
        console.log("Error");
        break;
}